package com.myfinances.domain.entity

/**
 * Перечисление для фильтрации транзакций по их типу.
 * Используется в UseCase'ах и ViewModel для запроса определенного вида операций:
 * только доходы, только расходы или все вместе.
 */
enum class TransactionTypeFilter {
    /**
     * Только доходные транзакции.
     */
    INCOME,

    /**
     * Только расходные транзакции.
     */
    EXPENSE,

    /**
     * Все транзакции (и доходы, и расходы).
     */
    ALL
}